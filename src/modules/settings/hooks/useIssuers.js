    defaultReturnPolicy: row.default_return_policy,
    defaultCancellationPolicy: row.default_cancellation_policy,
    defaultQualityGuarantee: row.default_quality_guarantee,
    defaultStorageTerms: row.default_storage_terms,
    defaultDeliveryDisclaimer: row.default_delivery_disclaimer,
    defaultForceMajeure: row.default_force_majeure,
    defaultPriceRevisionTerms: row.default_price_revision_terms,
    defaultConfidentialityTerms: row.default_confidentiality_terms,
    defaultGoverningLaw: row.default_governing_law,
